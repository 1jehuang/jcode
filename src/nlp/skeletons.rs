fn capitalize(s: &str) -> String {
    let mut chars = s.chars();
    match chars.next() {
        None => String::new(),
        Some(first) => {
            let upper = first.to_uppercase().collect::<String>();
            format!("{}{}", upper, chars.as_str())
        }
    }
}

