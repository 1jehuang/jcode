/// Generate a usage example
pub fn generate_usage_example(language: &str) -> String {
    match language.to_lowercase().as_str() {
        "rust" => "cargo run".to_string(),
        "python" => "python main.py".to_string(),
        "javascript" | "typescript" => "node main.js".to_string(),
        "java" => "javac Main.java && java Main".to_string(),
        "c++" | "cpp" => "g++ main.cpp -o main && ./main".to_string(),
        "go" => "go run main.go".to_string(),
        _ => format!("Run the {} file", language),
    }
}

