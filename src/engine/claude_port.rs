// [2] IDE 集成 — PID锁文件协议
// 移植自: utils/nativeInstaller/pidLock.ts
// 模式: 原子锁写入 + PID存活检测 + 二级校验
// ========================================================================

pub struct IdeLockFile {
    pub pid: u32,
    pub version: String,
    pub exec_path: PathBuf,
    pub acquired_at: SystemTime,
}

impl IdeLockFile {
    /// 写入PID锁文件 (原子)
    pub fn write(version: &str, exec_path: &Path) -> Result<PathBuf, String> {
        let pid = std::process::id();
        let lock_dir = dirs::home_dir()
            .ok_or("No home dir")?
            .join(".carpai")
            .join("versions");

        std::fs::create_dir_all(&lock_dir).map_err(|e| e.to_string())?;

        // 原子写入: .tmp.pid.ts → rename
        let ts = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_millis();
        let tmp_path = lock_dir.join(format!(".tmp.{}.{}", pid, ts));
        let final_path = lock_dir.join(format!("{}.lock", pid));

        let content = serde_json::json!({
            "pid": pid,
            "version": version,
            "execPath": exec_path.to_string_lossy(),
            "acquiredAt": ts,
        });

        std::fs::write(&tmp_path, serde_json::to_string_pretty(&content).map_err(|e| e.to_string())?)
            .map_err(|e| e.to_string())?;
        std::fs::rename(&tmp_path, &final_path).map_err(|e| e.to_string())?;

        Ok(final_path)
    }

    /// 扫描存活IDE锁 (二级校验)
    pub fn scan_alive(scan_dir: &Path) -> Vec<IdeLockFile> {
        let mut alive = Vec::new();
        if !scan_dir.exists() { return alive; }

        if let Ok(entries) = std::fs::read_dir(scan_dir) {
            for entry in entries.flatten() {
                let path = entry.path();
                if path.extension().map(|e| e == "lock").unwrap_or(false) {
                    if let Ok(content) = std::fs::read_to_string(&path) {
                        if let Ok(json) = serde_json::from_str::<serde_json::Value>(&content) {
                            let pid = json["pid"].as_u64().unwrap_or(0) as u32;
                            let version = json["version"].as_str().unwrap_or("").to_string();
                            let exec = json["execPath"].as_str().unwrap_or("");

                            // 二级校验: PID存活 + 进程名匹配
                            if Self::is_process_alive(pid) && Self::is_carpai_process(pid) {
                                alive.push(IdeLockFile {
                                    pid, version,
                                    exec_path: PathBuf::from(exec),
                                    acquired_at: SystemTime::now(),
                                });
                            } else {
                                // 清理过期锁
                                let _ = std::fs::remove_file(&path);
                            }
                        }
                    }
                }
            }
        }
        alive
    }

    #[cfg(unix)]
    fn is_process_alive(pid: u32) -> bool {
        unsafe { libc::kill(pid as i32, 0) == 0 }
    }

    #[cfg(windows)]
    fn is_process_alive(pid: u32) -> bool {
        use std::process::Command;
        Command::new("tasklist")
            .args(["/FI", &format!("PID eq {}", pid), "/NH"])
            .output()
            .map(|o| !o.stdout.is_empty())
            .unwrap_or(false)
    }

    fn is_carpai_process(pid: u32) -> bool {
        #[cfg(unix)]
        {
            let path = format!("/proc/{}/comm", pid);
            std::fs::read_to_string(&path)
                .map(|c| c.contains("carpai") || c.contains("jcode"))
                .unwrap_or(false)
        }
        #[cfg(windows)]
        {
            use std::process::Command;
            Command::new("tasklist")
                .args(["/FI", &format!("PID eq {}", pid), "/FO", "CSV"])
                .output()
                .map(|o| {
                    let out = String::from_utf8_lossy(&o.stdout);
                    out.contains("carpai") || out.contains("jcode")
                })
                .unwrap_or(false)
        }
    }
}

