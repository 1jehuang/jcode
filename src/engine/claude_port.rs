/// Agent 决策循环状态
#[derive(Debug, Clone, PartialEq)]
pub enum AgentLoopState {
    PreProcessing,
    CallingApi,
    ExecutingTools,
    PostProcessing,
    Completed,
    Error(String),
}

/// 循环过渡原因
#[derive(Debug, Clone, PartialEq)]
pub enum TransitionReason {
    Continue,
    CompactRetry,
    TokenBudgetContinuation,
    ModelFallback,
    Completed,
    StopHookBlocking,
}

/// Agent 自主循环 — 并发工具执行引擎
pub struct AgentLoop {
    state: AgentLoopState,
    max_concurrency: usize,
    registry: Arc<Registry>,
    tool_semaphore: Arc<Semaphore>,
    sibling_abort: Arc<RwLock<Option<watch::Sender<bool>>>>,
    stats: Arc<RwLock<LoopStats>>,
}

#[derive(Debug, Default, Clone)]
pub struct LoopStats {
    pub total_loops: u64,
    pub api_calls: u64,
    pub tool_calls: u64,
    pub compact_retries: u64,
    pub model_fallbacks: u64,
}

impl AgentLoop {
    pub fn new(registry: Arc<Registry>, max_concurrency: usize) -> Self {
        let (tx, _) = watch::channel(false);
        Self {
            state: AgentLoopState::PreProcessing,
            max_concurrency,
            registry,
            tool_semaphore: Arc::new(Semaphore::new(max_concurrency)),
            sibling_abort: Arc::new(RwLock::new(Some(tx))),
            stats: Arc::new(RwLock::new(LoopStats::default())),
        }
    }

    /// 主循环: 状态机驱动的 Agent 决策
    pub async fn run_loop(&self) -> AgentLoopState {
        loop {
            match self.state.clone() {
                AgentLoopState::PreProcessing => {
                    // 压缩/内存注入等前置处理
                    self.state = AgentLoopState::CallingApi;
                }
                AgentLoopState::CallingApi => {
                    // 调用 LLM API
                    let mut stats = self.stats.write().await;
                    stats.api_calls += 1;
                    self.state = AgentLoopState::ExecutingTools;
                }
                AgentLoopState::ExecutingTools => {
                    // 并行执行工具
                    self.state = AgentLoopState::PostProcessing;
                }
                AgentLoopState::PostProcessing => {
                    // 检查是否需要继续
                    let stats = self.stats.read().await;
                    if stats.api_calls < 10 {
                        self.state = AgentLoopState::PreProcessing;
                    } else {
                        self.state = AgentLoopState::Completed;
                        break;
                    }
                }
                AgentLoopState::Completed => break,
                AgentLoopState::Error(_) => break,
            }
        }
        self.state.clone()
    }

    /// 并发执行工具 (isConcurrencySafe 分区)
    pub async fn execute_tools_concurrent(&self, tools: &[ToolDef]) -> Vec<ToolResult> {
        let mut results = Vec::new();
        let mut batch = Vec::new();
        let mut all_safe = true;

        for tool in tools {
            if tool.is_concurrency_safe {
                if all_safe {
                    batch.push(tool);
                } else {
                    // 串行执行之前的批次
                    if !batch.is_empty() {
                        results.extend(self.execute_serially(&batch).await);
                    }
                    batch = vec![tool];
                    all_safe = true;
                }
            } else {
                if !batch.is_empty() {
                    results.extend(self.execute_parallel(&batch).await);
                }
                batch = vec![tool];
                all_safe = false;
            }
        }

        // 最后一批
        if !batch.is_empty() {
            if all_safe {
                results.extend(self.execute_parallel(&batch).await);
            } else {
                results.extend(self.execute_serially(&batch).await);
            }
        }

        results
    }

    /// 并发执行一批工具，支持兄弟取消
    async fn execute_parallel(&self, batch: &[&ToolDef]) -> Vec<ToolResult> {
        let mut handles = Vec::new();
        let abort_rx = {
            let guard = self.sibling_abort.read().await;
            guard.as_ref().map(|tx| tx.subscribe())
        };

        for tool in batch {
            let abort = abort_rx.clone();
            let permit = self.tool_semaphore.clone().acquire_owned().await.unwrap();
            let registry = self.registry.clone();
            let tool_name = tool.name.clone();
            let tool_input = tool.input.clone();
            let tool_ctx = tool.context.clone();

            handles.push(tokio::spawn(async move {
                let _permit = permit;
                let mut rx = abort;
                tokio::select! {
                    result = async {
                        match registry.execute(&tool_name, tool_input, tool_ctx).await {
                            Ok(output) => ToolResult { name: tool_name.clone(), success: true, output: output.text() },
                            Err(e) => ToolResult { name: tool_name.clone(), success: false, output: format!("Tool error: {}", e) },
                        }
                    } => result,
                    _ = async {
                        if let Some(ref mut r) = rx { r.changed().await.ok(); }
                    } => {
                        ToolResult { name: tool_name, success: false, output: "Cancelled: sibling tool error".into() }
                    }
                }
            }));
        }

        let mut results = Vec::new();
        for handle in handles {
            if let Ok(result) = handle.await {
                // Bash 错误触发兄弟取消
                if result.name == "bash" && !result.success {
                    if let Some(tx) = self.sibling_abort.read().await.as_ref() {
                        let _ = tx.send(true);
                    }
                }
                results.push(result);
            }
        }
        results
    }

    /// 串行执行一批工具（一个接一个，不并发）
    async fn execute_serially(&self, batch: &[&ToolDef]) -> Vec<ToolResult> {
        let mut results = Vec::new();
        for tool in batch {
            let registry = self.registry.clone();
            let result = match registry.execute(&tool.name, tool.input.clone(), tool.context.clone()).await {
                Ok(output) => ToolResult {
                    name: tool.name.clone(),
                    success: true,
                    output: output.text(),
                },
                Err(e) => ToolResult {
                    name: tool.name.clone(),
                    success: false,
                    output: format!("Tool error: {}", e),
                },
            };
            results.push(result);
        }
        results
    }

    /// Token Budget 检查 + 恢复
    pub fn check_token_budget(used: u64, max: u64) -> TransitionReason {
        if used > max {
            TransitionReason::TokenBudgetContinuation
        } else {
            TransitionReason::Continue
        }
    }
}

