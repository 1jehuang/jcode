pub struct ShellCompletion {
    pub name: String,
    pub rc_file: PathBuf,
    pub cache_file: PathBuf,
    pub completion_line: String,
    pub shell_flag: String,
}

impl ShellCompletion {
    pub fn detect() -> Vec<ShellCompletion> {
        let mut shells = Vec::new();

        // zsh
        if let Ok(path) = std::env::var("SHELL") {
            if path.contains("zsh") {
                if let Some(home) = dirs::home_dir() {
                    shells.push(ShellCompletion {
                        name: "zsh".into(),
                        rc_file: home.join(".zshrc"),
                        cache_file: home.join(".carpai").join("completion.zsh"),
                        completion_line: "source ~/.carpai/completion.zsh".into(),
                        shell_flag: "zsh".into(),
                    });
                }
            }
        }

        // bash
        if let Ok(path) = std::env::var("SHELL") {
            if path.contains("bash") {
                if let Some(home) = dirs::home_dir() {
                    let rc = if cfg!(windows) {
                        home.join(".bash_profile")
                    } else {
                        home.join(".bashrc")
                    };
                    shells.push(ShellCompletion {
                        name: "bash".into(),
                        rc_file: rc,
                        cache_file: home.join(".carpai").join("completion.bash"),
                        completion_line: "source ~/.carpai/completion.bash".into(),
                        shell_flag: "bash".into(),
                    });
                }
            }
        }

        shells
    }

    /// 幂等安装补全脚本 (原子写入)
    pub fn install(&self) -> Result<(), String> {
        // 创建补全缓存目录
        if let Some(parent) = self.cache_file.parent() {
            std::fs::create_dir_all(parent).map_err(|e| e.to_string())?;
        }

        // 原子写入: 先写 .tmp 再 rename
        let tmp = self.cache_file.with_extension("tmp");
        let content = format!(
            "# CarpAI shell completion for {}\n# Generated by carpai\ncomplete -C carpai carpai\n",
            self.name
        );
        std::fs::write(&tmp, &content).map_err(|e| e.to_string())?;
        std::fs::rename(&tmp, &self.cache_file).map_err(|e| e.to_string())?;

        // 幂等注入 rc 文件
        if self.rc_file.exists() {
            let rc_content = std::fs::read_to_string(&self.rc_file).map_err(|e| e.to_string())?;
            if !rc_content.contains(&self.completion_line) {
                let mut new_rc = rc_content;
                new_rc.push_str(&format!("\n# CarpAI completion\n{}\n", self.completion_line));
                std::fs::write(&self.rc_file, &new_rc).map_err(|e| e.to_string())?;
            }
        }

        Ok(())
    }

    /// Ghost Text 渲染: 根据上下文渲染不可见文本
    pub fn render_ghost_text(line_prefix: &str, suggestion: &str) -> String {
        // 只渲染尚未输入的部分
        if suggestion.starts_with(line_prefix) {
            suggestion[line_prefix.len()..].to_string()
        } else {
            suggestion.to_string()
        }
    }
}

