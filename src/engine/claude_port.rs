// [6] 错误自修复 — 三层恢复
// 移植自: query.ts + StreamingToolExecutor.ts + messages.ts
// 模式: Model Fallback → Memory Hint → Sibling Cancellation
// ========================================================================

pub struct ErrorRecoveryEngine;

impl ErrorRecoveryEngine {
    /// 第一层: API 错误 → 模型降级
    pub async fn model_fallback(error: &str, current_model: &str) -> Option<&'static str> {
        if error.contains("overloaded") || error.contains("529") || error.contains("rate_limit") {
            match current_model {
                "sonnet" => Some("haiku"),
                "opus" => Some("sonnet"),
                _ => None,
            }
        } else {
            None
        }
    }

    /// 第二层: 工具错误 → Memory Correction Hint
    pub fn memory_correction_hint(error_msg: &str) -> String {
        format!(
            "{}\n\n<system-reminder>\n\
             Note: The user's next message may contain a correction or preference. \
             Pay close attention — if they explain what went wrong or how they'd prefer \
             you to work, consider saving that to memory for future sessions.\n\
             </system-reminder>",
            error_msg
        )
    }

    /// 第三层: Prompt 过长 → Reactive Compact
    pub async fn reactive_compact(history: &[String]) -> Vec<String> {
        // 压缩对话历史: 保留最后 N 条，其余摘要
        let keep = 20.min(history.len());
        let mut compacted = history.iter().rev().take(keep).cloned().collect::<Vec<_>>();
        compacted.reverse();
        compacted
    }
}

