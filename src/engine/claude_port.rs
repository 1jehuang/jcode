// [5] 语义理解 — FileEditTool 降级链
// 移植自: tools/FileEditTool/utils.ts
// 模式: 精确→引号归一化→反清洗 降级
// ========================================================================

pub struct StringMatcher;

impl StringMatcher {
    /// 降级链查找: 精确 → 引号归一化 → 反清洗
    pub fn find_actual_string(content: &str, search: &str) -> Option<(usize, String)> {
        // Level 1: 精确匹配
        if let Some(pos) = content.find(search) {
            return Some((pos, search.to_string()));
        }

        // Level 2: 引号归一化
        let normalized = Self::normalize_quotes(search);
        if normalized != search {
            if let Some(pos) = content.find(&normalized) {
                // 从原文提取实际字符串
                let actual = &content[pos..pos + search.len().min(content.len() - pos)];
                return Some((pos, actual.to_string()));
            }
        }

        // Level 3: 反清洗 (处理被转义的XML标签)
        let desanitized = Self::desanitize(search);
        if desanitized != search {
            return Self::find_actual_string(content, &desanitized);
        }

        None
    }

    /// 引号归一化: 花引号 → 直引号
    pub fn normalize_quotes(s: &str) -> String {
        s.chars().map(|c| match c {
            '\u{2018}' | '\u{2019}' => '\'',  // ' '
            '\u{201C}' | '\u{201D}' => '"',  // " "
            '\u{00AB}' | '\u{00BB}' => '"',  // « »
            _ => c,
        }).collect()
    }

    /// 反清洗: 将模型可能输出的安全版本还原
    pub fn desanitize(s: &str) -> String {
        s.replace("<fnr>", "<function_results>")
         .replace("<n>", "<name>")
         .replace("< META_START >", "<META_START>")
         .replace("< META_END >", "<META_END>")
         .replace("&lt;", "<")
         .replace("&gt;", ">")
         .replace("&amp;", "&")
    }

    /// 尾部空格剥离
    pub fn strip_trailing_whitespace(s: &str) -> String {
        s.lines()
            .map(|l| l.trim_end())
            .collect::<Vec<_>>()
            .join("\n")
    }

    /// 文档注释提取
    pub fn extract_doc_comment(content: &str, line: usize) -> Option<String> {
        let lines: Vec<&str> = content.lines().collect();
        let mut docs = Vec::new();
        for i in (0..line.saturating_sub(1)).rev() {
            let l = lines.get(i)?.trim();
            if l.starts_with("///") {
                docs.push(l.trim_start_matches("///").trim());
            } else if l.is_empty() { continue; } else { break; }
        }
        docs.reverse();
        if docs.is_empty() { None } else { Some(docs.join("\n")) }
    }
}

