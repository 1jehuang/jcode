//! # Plan Mode - 计划模式工具
//!
//! 提供结构化的计划-执行工作流，确保所有操作经过审批：
//! - **模式切换** - 进入/退出计划模式
//! - **步骤管理** - 添加/审批/拒绝/完成步骤
//! - **安全保护** - 防止意外丢失未处理步骤
//! - **操作日志** - 完整的决策记录
//!
//! ## 使用场景
//!
//! 适用于需要谨慎操作的场景：
//! - 生产环境部署
//! - 数据库迁移
//! - 大规模重构
//! - 敏感配置变更
//!
//! ## 工作流程
//!
//! ```
//! 1. /plan on                    → 进入计划模式
//! 2. 添加步骤: 分析需求           → PlanStep (Pending)
//! 3. 添加步骤: 设计方案           → PlanStep (Pending)
//! 4. 添加步骤: 实现代码           → PlanStep ( Pending)
//! 5. 审批步骤 1                  → PlanStep (Approved)
//! 6. 执行步骤 1                  → PlanStep (Completed)
//! 7. ... 重复直到完成
//! 8. /plan off                   → 退出计划模式
//! ```

